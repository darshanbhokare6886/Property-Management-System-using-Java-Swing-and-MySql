            ResultSet rs = MyCon.retrieveData("select seller_name,seller_contact,plot_address,plot_area,plot_floorallowed,plot_type,plot_price from seller as s inner join plot as p  on s.seller_id=p.seller_id where plot_address='" + address + "'");

