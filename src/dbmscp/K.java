    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        String floor = (String) jComboBox2.getSelectedItem();
        String minPrice = null; // The minimum price selected by the user
        String maxPrice = null; // The maximum price selected by the user

        // Check which price range is selected
        String pri = (String) jComboBox3.getSelectedItem();
        switch (pri) {
            case "0-10 Lakhs":
                minPrice = "0";
                maxPrice = "1000000";
                break;
            case "10-20 Lakhs":
                minPrice = "1000000";
                maxPrice = "2000000";
                break;
            case "20-30 Lakhs":
                minPrice = "2000000";
                maxPrice = "3000000";
                break;
            // Add more cases for different price ranges as needed
        }
        String minArea = null;
        String maxArea = null;
        String area = (String) jComboBox1.getSelectedItem();
        switch (area) {
            case "0-10000sqft":
                minArea = "0";
                maxArea = "10000";
                break;
            case "10000-20000sqft":
                minArea = "10000";
                maxArea = "20000";
                break;
            case "20000-30000sqft":
                minArea = "20000";
                maxArea = "30000";
                break;
            // Add more cases for different price ranges as needed
        }

        try {
            // Build your SQL query dynamically based on the selected criteria
            String query = "select seller_name,seller_contact,plot_address,plot_area,plot_floorallowed,plot_type,plot_price from seller as s inner join plot as p  on s.seller_id=p.seller_id where plot_floorallowed='" + floor + "'";

            // Add price range condition
            if (minPrice != null && maxPrice != null) {
                query += " AND plot_price >= " + minPrice + " AND plot_price < " + maxPrice;
            }
            if (minPrice != null && maxPrice != null) {
                query += " AND plot_area >= " + minArea + " AND plot_area < " + maxArea;
            }

            ResultSet rs = MyCon.retrieveData(query);

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("seller_name"),
                    rs.getString("seller_contact"),
                    rs.getString("plot_address"),
                    rs.getString("plot_area"),
                    rs.getString("plot_floorallowed"),
                    rs.getString("plot_type"),
                    rs.getString("plot_price"),});
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

